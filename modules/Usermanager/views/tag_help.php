<strong>Usermanager usage</strong><br />
<strong>&lt;ion:usermanager /&gt;</strong> - Returns this help text.<br />
<strong>&lt;ion:usermanager request="minilogin" /&gt;</strong> - Returns a mini-view for login. E.g. for the upper right of a website.<br />
<strong>&lt;ion:usermanager request="login" /&gt;</strong> - Returns a normal sized login view.<br />
<strong>&lt;ion:usermanager request="register" /&gt;</strong> - Returns a registration form.<br />
<strong>&lt;ion:usermanager request="profile" /&gt;</strong> - Displays the profile of a user incl. editing.<br />
